# PostToolUse guard: warn when editing a Nix-generated ~/.claude file.
#
# Most of ~/.claude is deployed from this module (settings.json, skills/, agents/,
# output-styles/, CLAUDE.md). Editing those copies directly is silently reverted
# on the next `just qr` (the capture/deploy round-trip). Edit the source under
# modules/apps/cli/claude-code/config/ instead, or `claude-capture` a deliberate
# runtime change back first. Auto-memory (~/.claude/projects/*/memory/) and
# Claude-created worktrees are exempt -- they are legitimately writable at runtime.
#
# Wired into settings.json PostToolUse via cfg/activation.nix (Nix-owned, stripped
# from capture in cfg/fish.nix). jq on PATH via runtimeInputs. Warn-level (exit 0).

input="$(cat)"
file="$(jq -r '.tool_input.file_path // empty' <<<"$input" 2>/dev/null || true)"
[[ -n "$file" ]] || exit 0

claude_home="${CLAUDE_CONFIG_DIR:-$HOME/.claude}"
case "$file" in
  "$claude_home"/projects/*/memory/*) exit 0 ;; # auto-memory: writable
  */.claude/worktrees/*) exit 0 ;;              # Claude worktrees: writable
  "$claude_home"/*)
    echo "[generated-path] WARNING: $file is generated by the nixerator claude-code module; direct edits are reverted on the next 'just qr'. Edit the source under modules/apps/cli/claude-code/config/ then 'just qr', or 'claude-capture' a deliberate runtime change back."
    ;;
esac
exit 0
